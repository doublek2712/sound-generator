pub fn format_letter_octave(letter_octave: LetterOctave) -> String {
    let letter_name = match letter_octave.letter() {
        Letter::C => "C",
        Letter::Csh => "C#",
        Letter::Db => "Db",
        Letter::D => "D",
        Letter::Dsh => "D#",
        Letter::Eb => "Eb",
        Letter::E => "E",
        Letter::F => "F",
        Letter::Fsh => "F#",
        Letter::Gb => "Gb",
        Letter::G => "G",
        Letter::Gsh => "G#",
        Letter::Ab => "Ab",
        Letter::A => "A",
        Letter::Ash => "A#",
        Letter::Bb => "Bb",
        Letter::B => "B",
    };
    format!("{}{}", letter_name, letter_octave.octave())
}

